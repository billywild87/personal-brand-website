import { type SVGProps } from "react";

export type IconProps = SVGProps<SVGSVGElement> & {
  size?: number | string;
};

const defaultProps: IconProps = {
  size: 24,
  strokeWidth: 2,
  stroke: "currentColor",
  fill: "none",
  strokeLinecap: "round" as const,
  strokeLinejoin: "round" as const,
};

function createIcon(
  paths: React.ReactNode,
  displayName: string
): React.FC<IconProps> {
  const Icon: React.FC<IconProps> = ({ size, ...props }) => (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={size ?? defaultProps.size}
      height={size ?? defaultProps.size}
      viewBox="0 0 24 24"
      fill={defaultProps.fill}
      stroke={defaultProps.stroke}
      strokeWidth={defaultProps.strokeWidth}
      strokeLinecap={defaultProps.strokeLinecap}
      strokeLinejoin={defaultProps.strokeLinejoin}
      {...props}
    >
      {paths}
    </svg>
  );
  Icon.displayName = displayName;
  return Icon;
}

